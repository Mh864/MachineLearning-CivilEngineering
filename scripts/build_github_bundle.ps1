$ErrorActionPreference = "Stop"
$Root = Resolve-Path (Join-Path $PSScriptRoot "..")
$Dest = Join-Path $Root "github_upload_bundle"

Write-Host "Project root: $Root"
Write-Host "Bundle dest:  $Dest"

if (Test-Path $Dest) {
  Remove-Item $Dest -Recurse -Force
}
New-Item -ItemType Directory -Path $Dest -Force | Out-Null

function Get-RobocopyExe {
  $candidates = @(
    (Join-Path $env:SystemRoot "System32\robocopy.exe"),
    (Join-Path $env:WINDIR "System32\robocopy.exe")
  )
  foreach ($c in $candidates) {
    if (Test-Path -LiteralPath $c) { return $c }
  }
  return $null
}

function Copy-TreeNoRobocopy {
  param([string]$Source, [string]$Target, [string[]]$ExcludeDirs)
  if (-not (Test-Path $Source)) { return }
  New-Item -ItemType Directory -Path $Target -Force | Out-Null
  Get-ChildItem -LiteralPath $Source -Force | ForEach-Object {
    if ($_.PSIsContainer) {
      if ($ExcludeDirs -contains $_.Name) { return }
      Copy-TreeNoRobocopy -Source $_.FullName -Target (Join-Path $Target $_.Name) -ExcludeDirs $ExcludeDirs
    } else {
      Copy-Item -LiteralPath $_.FullName -Destination (Join-Path $Target $_.Name) -Force
    }
  }
}

function Copy-RepoDir {
  param([string]$RelativePath, [string[]]$ExcludeDirs = @("__pycache__", ".pytest_cache"))
  $src = Join-Path $Root $RelativePath
  if (-not (Test-Path $src)) {
    Write-Warning "Skip missing: $RelativePath"
    return
  }
  $dst = Join-Path $Dest $RelativePath
  New-Item -ItemType Directory -Path $dst -Force | Out-Null
  $rc = Get-RobocopyExe
  if ($rc) {
    $rcArgs = @($src, $dst, "/E", "/NFL", "/NDL", "/NJH", "/NJS", "/NC", "/NS")
    if ($ExcludeDirs.Count -gt 0) {
      $rcArgs += "/XD"
      $rcArgs += $ExcludeDirs
    }
    & $rc @rcArgs | Out-Null
    if ($LASTEXITCODE -ge 8) {
      throw "robocopy failed for $RelativePath (code $LASTEXITCODE)"
    }
  } else {
    Copy-TreeNoRobocopy -Source $src -Target $dst -ExcludeDirs $ExcludeDirs
  }
}

function Copy-TreeFull {
  param([string]$Source, [string]$Target)
  if (-not (Test-Path $Source)) { return }
  New-Item -ItemType Directory -Path $Target -Force | Out-Null
  $rc = Get-RobocopyExe
  if ($rc) {
    & $rc $Source $Target /E /NFL /NDL /NJH /NJS /NC /NS | Out-Null
    if ($LASTEXITCODE -ge 8) { throw "robocopy failed: $Source" }
  } else {
    Copy-TreeNoRobocopy -Source $Source -Target $Target -ExcludeDirs @()
  }
}

