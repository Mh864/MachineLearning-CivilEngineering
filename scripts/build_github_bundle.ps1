if (Test-Path $noaaSrc) {
  $noaaDst = Join-Path $Dest "data\raw\noaa"
  Copy-TreeFull -Source $noaaSrc -Target $noaaDst
}

$usgsSrc = Join-Path $Root "data\raw\usgs"
if (Test-Path $usgsSrc) {
  $usgsDst = Join-Path $Dest "data\raw\usgs"
  Copy-TreeFull -Source $usgsSrc -Target $usgsDst
}

# --- Models & metrics ---
$modelsDst = Join-Path $Dest "models"
New-Item -ItemType Directory -Path $modelsDst -Force | Out-Null
foreach ($f in @("model.pkl", "lgbm_model.pkl")) {
  $p = Join-Path $Root "models\$f"
  if (Test-Path $p) { Copy-Item $p $modelsDst -Force }
